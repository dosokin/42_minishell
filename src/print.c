#include "minishell.h"

void	print_escape(char *envp)
{
	int	i;

	i = -1;
	while (envp[++i])
	{
		if (envp[i] == '\\' || envp[i] == '$' || envp[i] == '\"')
			ft_putchar_fd('\\', 1);
		ft_putchar_fd(envp[i], 1);
	}
}
